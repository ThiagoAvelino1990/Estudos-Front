/**
 * Condição while
 * Laço de repetição com teste lógico no início
 */
var p = 10; //valor da condição
var c = 1; //contador

while(c <= p){
    console.log(`Passo do c ${c}`);
    c++;
}

/**
 * Laço de repetição com teste lógico no final
 */

var c2 = 1

do{
    console.log(`Passo do c2 ${c2}`);
    c2++;
}while(c2 <= p);